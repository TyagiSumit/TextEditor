    private void OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenActionPerformed
        
         int returnVal = fileChooser.showOpenDialog(this);
         if (returnVal == JFileChooser.APPROVE_OPTION) {
             File file = fileChooser.getSelectedFile();
             try {
              // What to do with the file, e.g. display it in a TextArea
           Text.read( new FileReader( file.getAbsolutePath() ), null );
         } catch (IOException ex) {
               System.out.println("problem accessing file"+file.getAbsolutePath());
             }
         } 
         else {
             System.out.println("File access cancelled by user.");
         }
    }//GEN-LAST:event_OpenActionPerformed

    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
       int returnVal = fileChooser.showSaveDialog(this);
         if (returnVal == JFileChooser.APPROVE_OPTION) {
             File file = fileChooser.getSelectedFile();
             try {
              // What to do with the file, e.g. display it in a TextArea
              if(jTabbedPane1.getSelectedIndex()==0){
                  JScrollPane s=(JScrollPane)jTabbedPane1.getComponentAt(jTabbedPane1.getSelectedIndex());
                JViewport viewport = s.getViewport(); 
                JTextArea T1 = (JTextArea)viewport.getView();
                PrintWriter p=new PrintWriter(file);
                    //p.write(Text.getText());
                    p.write(T1.getText());
                    p.flush();
                    p.close();
              }  
              else{
                  JTextArea T;
                T=(JTextArea)jTabbedPane1.getComponentAt(jTabbedPane1.getSelectedIndex());
              PrintWriter p=new PrintWriter(file);
                    //p.write(Text.getText());
                    p.write(T.getText());
                    p.flush();
                    p.close();
              }
         } catch (IOException ex) {
               System.out.println("problem accessing file"+file.getAbsolutePath());
              
            }
         } 
         else {
             System.out.println("File access cancelled by user.");
         }
    }//GEN-LAST:event_SaveActionPerformed

