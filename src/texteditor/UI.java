    private void OpenButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenButtonActionPerformed
        int returnVal = fileChooser.showOpenDialog(this);
         if (returnVal == JFileChooser.APPROVE_OPTION) {
             File file = fileChooser.getSelectedFile();
             try {
//              What to do with the file, e.g. display it in a TextArea
//           Text.read( new FileReader( file.getAbsolutePath() ), null );

                    JTextArea T=new JTextArea();
                    T.read(new FileReader(file.getAbsoluteFile()),null);
                    jTabbedPane1.addTab("Untitled"+tabcount, T);
                    jTabbedPane1.setTabComponentAt(tabcount,getTitlePanel(file.getName()));
                    tabcount++;
                    
         } catch (IOException ex) {
               System.out.println("problem accessing file"+file.getAbsolutePath());
             }
         } 
         else {
             System.out.println("File access cancelled by user.");
         }
    }//GEN-LAST:event_OpenButtonActionPerformed

